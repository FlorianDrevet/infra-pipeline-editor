    private static string GenerateSharedDeployStep()
    {
        var sb = new StringBuilder();
        sb.AppendLine("# Shared Deploy Step Template — Auto-generated by InfraFlowSculptor");
        sb.AppendLine();
        sb.AppendLine("parameters:");
        sb.AppendLine("  - name: azureResourceManagerConnection");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: subscriptionId");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: location");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: deploymentName");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: templateFile");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: templateParametersFile");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: overrideParameters");
        sb.AppendLine("    type: string");
        sb.AppendLine("    default: ''");
        sb.AppendLine();
        sb.AppendLine("steps:");
        sb.AppendLine("  - pwsh: |");
        sb.AppendLine("      $templateFile = '${{ parameters.templateFile }}'");
        sb.AppendLine("      $parametersFile = '${{ parameters.templateParametersFile }}'");
        sb.AppendLine();
        sb.AppendLine("      if (-not (Test-Path -LiteralPath $templateFile)) {");
        sb.AppendLine("        $templateDirectory = Split-Path -Path $templateFile -Parent");
        sb.AppendLine("        if (Test-Path -LiteralPath $templateDirectory) {");
        sb.AppendLine("          Write-Host \"Template directory contents:\"");
        sb.AppendLine("          Get-ChildItem -LiteralPath $templateDirectory -Recurse | Select-Object FullName | Format-Table -AutoSize | Out-String | Write-Host");
        sb.AppendLine("        }");
        sb.AppendLine("        throw \"Template file not found: $templateFile\"");
        sb.AppendLine("      }");
        sb.AppendLine();
        sb.AppendLine("      if (-not (Test-Path -LiteralPath $parametersFile)) {");
        sb.AppendLine("        $parametersDirectory = Split-Path -Path $parametersFile -Parent");
        sb.AppendLine("        $fallbackFiles = @()");
        sb.AppendLine("        if (Test-Path -LiteralPath $parametersDirectory) {");
        sb.AppendLine("          Write-Host \"Parameters directory contents:\"");
        sb.AppendLine("          Get-ChildItem -LiteralPath $parametersDirectory -Recurse | Select-Object FullName | Format-Table -AutoSize | Out-String | Write-Host");
        sb.AppendLine("          $fallbackFiles = @(Get-ChildItem -LiteralPath $parametersDirectory -Filter 'main.*.bicepparam' -File)");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine("        if ($fallbackFiles.Count -eq 1) {");
        sb.AppendLine("          $parametersFile = $fallbackFiles[0].FullName");
        sb.AppendLine("          Write-Warning \"Expected parameters file not found. Falling back to: $parametersFile\"");
        sb.AppendLine("        }");
        sb.AppendLine("        else {");
        sb.AppendLine("          throw \"Parameters file not found: $parametersFile\"");
        sb.AppendLine("        }");
        sb.AppendLine("      }");
        sb.AppendLine();
        sb.AppendLine("      $resolvedTemplateFile = (Resolve-Path -LiteralPath $templateFile).Path");
        sb.AppendLine("      $resolvedParametersFile = (Resolve-Path -LiteralPath $parametersFile).Path");
        sb.AppendLine("      $compiledTemplateFile = $resolvedTemplateFile");
        sb.AppendLine("      $compiledParametersFile = $resolvedParametersFile");
        sb.AppendLine("      $parametersFileForCompilation = $resolvedParametersFile");
        sb.AppendLine();
        sb.AppendLine("      if ([System.IO.Path]::GetExtension($resolvedTemplateFile).Equals('.bicep', [System.StringComparison]::OrdinalIgnoreCase)) {");
        sb.AppendLine("        $compiledTemplateFile = [System.IO.Path]::ChangeExtension($resolvedTemplateFile, '.json')");
