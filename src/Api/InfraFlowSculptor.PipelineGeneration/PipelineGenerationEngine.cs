    {
        var sb = new StringBuilder();
        sb.AppendLine("# PR Validation Pipeline — Auto-generated by InfraFlowSculptor");
        sb.AppendLine("name: $(date:yyyyMMdd)");
        sb.AppendLine();
        sb.AppendLine("pr:");
        sb.AppendLine("  branches:");
        sb.AppendLine("    include:");
        sb.AppendLine("      - main");
        sb.AppendLine("      - develop");
        sb.AppendLine("      - release/*");
        sb.AppendLine("  paths:");
        sb.AppendLine("    include:");
        sb.AppendLine($"      - {configName}/*");
        sb.AppendLine();
        sb.AppendLine("extends:");
        sb.AppendLine("  template: ../.azuredevops/pipelines/pr.pipeline.yml");
        sb.AppendLine("  parameters:");
        sb.AppendLine($"    projectName: {configName}");

        return sb.ToString();
    }

