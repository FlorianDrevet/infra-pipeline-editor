        sb.AppendLine("  - name: location");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: deploymentName");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: templateFile");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: templateParametersFile");
        sb.AppendLine("    type: string");
