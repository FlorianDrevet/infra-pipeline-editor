    private static string GenerateCommonVariables(GenerationRequest request)
    {
        var sb = new StringBuilder();
        sb.AppendLine("# Common Variables — Auto-generated by InfraFlowSculptor");
        sb.AppendLine();
        sb.AppendLine("variables:");

        // Use the location from the first environment as default, or "westeurope"
        var defaultLocation = request.Environments.Count > 0
            ? request.Environments[0].Location
            : "westeurope";
        sb.AppendLine($"  location: '{defaultLocation}'");

        return sb.ToString();
    }

    private static string GenerateEnvironmentVariables(EnvironmentDefinition env)
    {
        var sb = new StringBuilder();
        sb.AppendLine($"# {env.Name} Variables — Auto-generated by InfraFlowSculptor");
        sb.AppendLine();
        sb.AppendLine("variables:");

        if (!string.IsNullOrEmpty(env.AzureResourceManagerConnection))
        {
            sb.AppendLine($"  azureResourceManagerConnection: '{env.AzureResourceManagerConnection}'");
        }

        if (!string.IsNullOrEmpty(env.SubscriptionId))
        {
            sb.AppendLine($"  subscriptionId: '{env.SubscriptionId}'");
        }

        if (!string.IsNullOrEmpty(env.Location))
        {
            sb.AppendLine($"  location: '{env.Location}'");
        }

        return sb.ToString();
    }
}
