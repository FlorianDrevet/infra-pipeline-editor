    private static string GenerateSharedDeployStep()
    {
        var sb = new StringBuilder();
        sb.AppendLine("# Shared Deploy Step Template — Auto-generated by InfraFlowSculptor");
        sb.AppendLine();
        sb.AppendLine("parameters:");
        sb.AppendLine("  - name: azureResourceManagerConnection");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: subscriptionId");
        sb.AppendLine("    type: string");
        sb.AppendLine("  - name: resourceGroupName");
