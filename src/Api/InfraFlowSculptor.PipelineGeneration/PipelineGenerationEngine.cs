    private static string GenerateSharedPrTemplate(
        IReadOnlyList<string> configNames,
        string? agentPoolName,
        string? bicepBasePath,
        string? pipelineBasePath)
    {
        var checkoutDirectories = new[]
        {
            BuildRepoRelativePath(pipelineBasePath, ".azuredevops"),
            BuildRepoRelativePath(bicepBasePath, "${{ parameters.projectName }}"),
        }.Distinct().ToList();

        var sb = new StringBuilder();
        sb.AppendLine("# Shared PR Validation Pipeline Template — Auto-generated by InfraFlowSculptor");
        sb.AppendLine();
        sb.AppendLine("parameters:");
        sb.AppendLine("  - name: projectName");
        sb.AppendLine("    type: string");
        sb.AppendLine("    values:");
        foreach (var name in configNames)
        {
            sb.AppendLine($"      - {name}");
        }
        sb.AppendLine("  - name: bicepFiles");
        sb.AppendLine("    type: object");
        sb.AppendLine("    default: [ main.bicep ]");
        sb.AppendLine("  - name: preparationSteps");
        sb.AppendLine("    type: stepList");
        sb.AppendLine("    default: []");
        sb.AppendLine("  - name: additionalSteps");
        sb.AppendLine("    type: stepList");
        sb.AppendLine("    default: []");
        sb.AppendLine();
        sb.AppendLine("variables:");
        sb.AppendLine("  - name: workingDirectory");
        sb.AppendLine($"    value: {BuildSourcesDirectoryPath(BuildRepoRelativePath(bicepBasePath, "${{ parameters.projectName }}"))}");
        sb.AppendLine();
        sb.AppendLine("stages:");
        sb.AppendLine("  - stage: validate");
        sb.AppendLine("    displayName: Validate");
        sb.AppendLine();
        sb.AppendLine("    jobs:");
        sb.AppendLine("      - job: validate");
        sb.AppendLine("        displayName: Validate");
        AppendPool(sb, agentPoolName);
        sb.AppendLine();
        sb.AppendLine("        steps:");
        sb.AppendLine("          - ${{ parameters.preparationSteps }}");
        sb.AppendLine();
        sb.AppendLine("          - template: ../steps/checkout.step.yml");
        sb.AppendLine("            parameters:");
        sb.AppendLine("              directories:");
        foreach (var directory in checkoutDirectories)
        {
            sb.AppendLine($"                - {directory}");
        }
        sb.AppendLine();
        sb.AppendLine("          - pwsh: |");
        sb.AppendLine($"              $configPath = \"{BuildSourcesDirectoryPath(BuildRepoRelativePath(bicepBasePath, "bicepconfig.json"))}\"");
        sb.AppendLine("              if (Test-Path $configPath) {");
        sb.AppendLine("                $config = Get-Content -Path $configPath | ConvertFrom-Json");
        sb.AppendLine("                $config.analyzers.core.rules.'use-recent-api-versions'.level = \"warning\"");
        sb.AppendLine("                $config | ConvertTo-Json -Depth 99 | Set-Content -Path $configPath");
        sb.AppendLine("              }");
        sb.AppendLine("            displayName: Update Bicep Config");
        sb.AppendLine("            name: UpdateBicepConfig");
        sb.AppendLine();
        sb.AppendLine("          - ${{ each bicepFile in parameters.bicepFiles }}:");
        sb.AppendLine("            - script: |");
        sb.AppendLine("                az bicep build --file \"$(workingDirectory)/${{ bicepFile }}\"");
        sb.AppendLine("              displayName: Lint ${{ bicepFile }}");
        sb.AppendLine();
        sb.AppendLine("          - ${{ parameters.additionalSteps }}");

        return sb.ToString();
    }

