using InfraFlowSculptor.GenerationCore.Models;
using InfraFlowSculptor.PipelineGeneration.Models;

namespace InfraFlowSculptor.PipelineGeneration;

/// <summary>
/// Main engine for Azure DevOps pipeline YAML generation.
/// Orchestrates resource-type generators and assembles the final pipeline output.
/// Stub implementation — pipeline content format will be defined later.
/// </summary>
public sealed class PipelineGenerationEngine
{
    /// <summary>
    /// Generates Azure DevOps pipeline YAML files from the given generation request.
    /// </summary>
    /// <param name="request">The shared generation request containing infrastructure configuration data.</param>
    /// <returns>The generated pipeline files.</returns>
    public PipelineGenerationResult Generate(GenerationRequest request)
    {
        // Stub — pipeline content format will be implemented in a future iteration.
        // For now, generate a minimal placeholder pipeline.
        var mainYaml = GeneratePlaceholderPipeline(request);

        return new PipelineGenerationResult
        {
            MainPipelineYaml = mainYaml,
            TemplateFiles = new Dictionary<string, string>(),
        };
    }

    private static string GeneratePlaceholderPipeline(GenerationRequest request)
    {
        var resourceGroups = string.Join(", ", request.ResourceGroups.Select(rg => rg.Name));
        var environments = string.Join(", ", request.EnvironmentNames);

        return $$"""
                 # Azure DevOps Pipeline — Auto-generated by InfraFlowSculptor
                 # Resource Groups: {{resourceGroups}}
                 # Environments: {{environments}}
                 
                 trigger:
                   branches:
                     include:
                       - main
                 
                 pool:
                   vmImage: 'ubuntu-latest'
                 
                 stages:
                   - stage: Validate
                     displayName: 'Validate Infrastructure'
                     jobs:
                       - job: ValidateBicep
                         displayName: 'Validate Bicep Templates'
                         steps:
                           - task: AzureCLI@2
                             displayName: 'Validate Bicep'
                             inputs:
                               azureSubscription: '$(azureServiceConnection)'
                               scriptType: 'bash'
                               scriptLocation: 'inlineScript'
                               inlineScript: |
                                 az bicep build --file main.bicep
                 """;
    }
}
