        sb.AppendLine("                throw \"Artifact staging directory not found: $artifactRoot\"");
        sb.AppendLine("              }");
        sb.AppendLine();
        sb.AppendLine("              Write-Host \"Artifact staging contents:\"");
        sb.AppendLine("              Get-ChildItem -LiteralPath $artifactRoot -Recurse | Select-Object FullName | Format-Table -AutoSize | Out-String | Write-Host");
        sb.AppendLine();
        sb.AppendLine("              if (-not (Test-Path -LiteralPath $commonPath)) {");
        sb.AppendLine("                throw \"Common artifact folder not found: $commonPath\"");
        sb.AppendLine("              }");
        sb.AppendLine();
        sb.AppendLine("              if (-not (Test-Path -LiteralPath $projectPath)) {");
        sb.AppendLine("                throw \"Project artifact folder not found: $projectPath\"");
        sb.AppendLine("              }");
        sb.AppendLine();
        sb.AppendLine("              if (-not (Test-Path -LiteralPath $mainBicepPath)) {");
        sb.AppendLine("                throw \"Main Bicep file not found in artifact: $mainBicepPath\"");
        sb.AppendLine("              }");
        sb.AppendLine("            displayName: 'Validate artifact contents'");
        sb.AppendLine();
        sb.AppendLine("          - task: PublishBuildArtifacts@1");
        sb.AppendLine("            displayName: 'Publish Artifact'");
        sb.AppendLine("            inputs:");
        sb.AppendLine("              PathtoPublish: '$(Build.ArtifactStagingDirectory)'");
        sb.AppendLine("              ArtifactName: 'drop'");
        sb.AppendLine("              publishLocation: 'Container'");

        return sb.ToString();
    }

    private static string BuildRepoRelativePath(string? basePath, string relativePath)
    {
        if (string.IsNullOrWhiteSpace(basePath))
        {
            return relativePath;
        }

        return $"{basePath.Trim('/')}/{relativePath}";
    }

    private static string BuildSourcesDirectoryPath(string? relativePath)
    {
        if (string.IsNullOrWhiteSpace(relativePath))
        {
            return "$(Build.SourcesDirectory)";
        }

        return $"$(Build.SourcesDirectory)/{relativePath.Trim('/')}";
    }

    /// <summary>
    /// Builds the overrideParameters string from all variable group mappings.
    /// Format: <c>-bicepParam $(PIPELINE_VAR) -bicepParam2 $(PIPELINE_VAR2)</c>
    /// </summary>
    private static string BuildOverrideParameters(
        IReadOnlyList<GenerationCore.Models.PipelineVariableGroupDefinition> variableGroups)
    {
        var allMappings = variableGroups
            .SelectMany(g => g.Mappings)
            .ToList();

        if (allMappings.Count == 0)
            return string.Empty;

        return string.Join(" ", allMappings.Select(m => $"-{m.BicepParameterName} $({m.PipelineVariableName})"));
    }

