    {
        var checkoutDirectories = new[]
        {
            BuildRepoRelativePath(pipelineBasePath, ".azuredevops"),
            BuildRepoRelativePath(bicepBasePath, "Common"),
            BuildRepoRelativePath(bicepBasePath, "${{ parameters.projectName }}"),
        }.Distinct().ToList();

        var sb = new StringBuilder();
        sb.AppendLine("# Shared CI Pipeline Template — Auto-generated by InfraFlowSculptor");
        sb.AppendLine();
        sb.AppendLine("parameters:");
        sb.AppendLine("  - name: continueOnError");
        sb.AppendLine("    type: boolean");
        sb.AppendLine("    default: false");
        sb.AppendLine("  - name: projectName");
        sb.AppendLine("    type: string");
        sb.AppendLine("    values:");
        foreach (var name in configNames)
        {
            sb.AppendLine($"      - {name}");
        }
        sb.AppendLine();
        sb.AppendLine("stages:");
        sb.AppendLine("  - stage: Build");
        sb.AppendLine("    displayName: 'Build & Publish Artifacts'");
        sb.AppendLine("    jobs:");
        sb.AppendLine("      - job: BuildBicep");
        sb.AppendLine("        displayName: 'Build Bicep'");
        AppendPool(sb, agentPoolName);
