using System.Text;
using InfraFlowSculptor.GenerationCore.Models;

namespace InfraFlowSculptor.PipelineGeneration.Infra.Stages;

/// <summary>
/// Emits the <c>release.pipeline.yml</c> per-configuration release pipeline file.
/// Always executes regardless of mono-repo mode.
/// </summary>
public sealed class ReleasePipelineStage : IInfraPipelineStage
{
    /// <inheritdoc />
    public int Order => 300;

    /// <inheritdoc />
    public void Execute(InfraPipelineContext context)
    {
        context.Files["release.pipeline.yml"] = GenerateConfigReleasePipeline(
            context.Request, context.ConfigName, context.Request.AgentPoolName, context.IsMonoRepo);
    }

    private static string GenerateConfigReleasePipeline(GenerationRequest request, string configName, string? agentPoolName, bool isMonoRepo)
    {
        var sb = new StringBuilder();
        sb.AppendLine("# Release Pipeline — Auto-generated by InfraFlowSculptor");
        sb.AppendLine("name: $(date:yyyyMMdd)");
        sb.AppendLine();
        sb.AppendLine("trigger: none");
        sb.AppendLine();

        // Reference the CI pipeline to download its published artifact
        sb.AppendLine("resources:");
        sb.AppendLine("  pipelines:");
        sb.AppendLine("    - pipeline: ci");
        sb.AppendLine($"      source: '{configName} - CI'");
        sb.AppendLine("      trigger: none");
        sb.AppendLine();

        sb.AppendLine("variables:");

        // Environment list
        var envList = string.Join(",", request.Environments.Select(e => e.ShortName.ToLowerInvariant()));
        sb.AppendLine($"  - name: environments");
        sb.AppendLine($"    value: {envList}");

        sb.AppendLine();
        PipelineGenerationEngine.AppendPool(sb, agentPoolName, string.Empty);
        sb.AppendLine();
        sb.AppendLine("stages:");
        sb.AppendLine("  - ${{ each environment in split(variables.environments, ',') }}:");
        sb.AppendLine("      - stage: \"Deploy_${{ environment }}\"");
        sb.AppendLine("        displayName: \"Deploy ${{ environment }}\"");

        var hasStageVariables = !isMonoRepo || request.PipelineVariableGroups.Count > 0;

        if (hasStageVariables)
        {
            sb.AppendLine("        variables:");
        }

        if (!isMonoRepo)
        {
            // Reference config-local variables (single-config mode)
            sb.AppendLine("          - template: variables/${{ environment }}.yml");
            sb.AppendLine("          - template: variables/vars.yml");
        }

        // Emit variable group references (expand {env} placeholder to Azure DevOps template expression)
        foreach (var group in request.PipelineVariableGroups)
        {
            var resolvedName = group.GroupName.Replace("{env}", "${{ environment }}", StringComparison.OrdinalIgnoreCase);
            sb.AppendLine($"          - group: {resolvedName}");
        }
        sb.AppendLine();
        sb.AppendLine("        jobs:");
        sb.AppendLine($"          - template: {(isMonoRepo ? "../Common/jobs/deploy.job.yml" : "../.azuredevops/jobs/deploy.job.yml")}");
        sb.AppendLine("            parameters:");
        sb.AppendLine($"              projectName: {configName}");
        sb.AppendLine("              environment: ${{ environment }}");
        sb.AppendLine("              deployments:");

        // One deployment entry — the main Bicep subscription-level deployment
        sb.AppendLine($"                - displayName: \"Deploy {configName} Infrastructure\"");
        sb.AppendLine($"                  deploymentName: {configName.ToLowerInvariant()}-infra-$(Build.BuildNumber)");
        sb.AppendLine($"                  templateFile: $(Pipeline.Workspace)/ci/drop/{configName}/main.bicep");
        sb.AppendLine($"                  templateParametersFile: $(Pipeline.Workspace)/ci/drop/{configName}/parameters/main.${{{{environment}}}}.bicepparam");

        // Emit overrideParameters from variable group mappings + secure parameters
        var overrideParams = BuildOverrideParameters(request.PipelineVariableGroups, request.SecureParameterOverrides);
        if (!string.IsNullOrEmpty(overrideParams))
        {
            sb.AppendLine($"                  overrideParameters: {overrideParams}");
        }

        return sb.ToString();
    }

    /// <summary>
    /// Builds the overrideParameters string from variable group mappings and secure parameter overrides.
    /// </summary>
    private static string BuildOverrideParameters(
        IReadOnlyList<PipelineVariableGroupDefinition> variableGroups,
        IReadOnlyList<string> secureParameterOverrides)
    {
        var parts = new List<string>();

        parts.AddRange(
            variableGroups
                .SelectMany(g => g.Mappings)
                .Select(m => $"-{m.BicepParameterName} $({m.PipelineVariableName})"));

        parts.AddRange(
            secureParameterOverrides
                .Select(p => $"-{p} $({p})"));

        return parts.Count == 0 ? string.Empty : string.Join(" ", parts);
    }
}
