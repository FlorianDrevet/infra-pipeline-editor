    private static void AppendEnvironmentVariables(
        StringBuilder sb,
        EnvironmentDefinition env,
        AppPipelineGenerationRequest request)
    {
        var envKey = env.ShortName.ToLowerInvariant();
        var variablesPath = GetEnvironmentVariablesPath(envKey, request.IsMonoRepo);

        sb.AppendLine($"      - template: {variablesPath}");

        foreach (var group in request.PipelineVariableGroups)
        {
            var resolvedName = group.GroupName.Replace(
                "{env}", envKey, StringComparison.OrdinalIgnoreCase);
            sb.AppendLine($"      - group: {resolvedName}");
        }
    }

    private static void AppendSdkSetupStep(StringBuilder sb, string? runtimeStack, string? runtimeVersion)
    {
        var stack = runtimeStack?.ToUpperInvariant() ?? "DOTNETCORE";
        var version = runtimeVersion ?? "8.0";

        switch (stack)
        {
            case "DOTNETCORE" or "DOTNET":
                sb.AppendLine("          - task: UseDotNet@2");
                sb.AppendLine("            displayName: 'Setup .NET SDK'");
                sb.AppendLine("            inputs:");
                sb.AppendLine($"              packageType: sdk");
                sb.AppendLine($"              version: {version}.x");
                sb.AppendLine();
                break;

            case "NODE" or "NODEJS":
                sb.AppendLine("          - task: UseNode@1");
                sb.AppendLine("            displayName: 'Setup Node.js'");
                sb.AppendLine("            inputs:");
                sb.AppendLine($"              version: {version}.x");
                sb.AppendLine();
                break;

            case "PYTHON":
                sb.AppendLine("          - task: UsePythonVersion@0");
                sb.AppendLine("            displayName: 'Setup Python'");
                sb.AppendLine("            inputs:");
                sb.AppendLine($"              versionSpec: {version}");
                sb.AppendLine();
                break;

            case "JAVA":
                sb.AppendLine("          - task: JavaToolInstaller@0");
                sb.AppendLine("            displayName: 'Setup Java'");
                sb.AppendLine("            inputs:");
                sb.AppendLine($"              versionSpec: {version}");
                sb.AppendLine("              jdkArchitectureOption: x64");
                sb.AppendLine("              jdkSourceOption: PreInstalled");
                sb.AppendLine();
                break;

            default:
                sb.AppendLine($"          # Runtime setup: {stack} {version} — configure SDK manually");
                sb.AppendLine();
                break;
        }
    }

    private static void AppendDefaultBuildSteps(StringBuilder sb, string? runtimeStack, string srcPath)
    {
        var stack = runtimeStack?.ToUpperInvariant() ?? "DOTNETCORE";

        switch (stack)
        {
            case "DOTNETCORE" or "DOTNET":
                sb.AppendLine("          - script: |");
                sb.AppendLine($"              dotnet restore");
                sb.AppendLine($"              dotnet build --configuration Release --no-restore");
                sb.AppendLine($"              dotnet test --configuration Release --no-build --verbosity normal");
                sb.AppendLine($"              dotnet publish --configuration Release --no-build --output $(Build.ArtifactStagingDirectory)/drop");
                sb.AppendLine("            displayName: 'Restore, Build, Test & Publish'");
                sb.AppendLine($"            workingDirectory: {srcPath}");
                sb.AppendLine();
                break;

            case "NODE" or "NODEJS":
                sb.AppendLine("          - script: |");
                sb.AppendLine("              npm ci");
                sb.AppendLine("              npm run build");
                sb.AppendLine("              npm run test -- --ci || true");
                sb.AppendLine("            displayName: 'Install, Build & Test'");
                sb.AppendLine($"            workingDirectory: {srcPath}");
                sb.AppendLine();
                sb.AppendLine("          - script: |");
                sb.AppendLine($"              cp -r {srcPath}/dist $(Build.ArtifactStagingDirectory)/drop || cp -r {srcPath}/build $(Build.ArtifactStagingDirectory)/drop");
                sb.AppendLine("            displayName: 'Copy Build Output'");
                sb.AppendLine();
                break;

            case "PYTHON":
                sb.AppendLine("          - script: |");
                sb.AppendLine("              pip install -r requirements.txt");
                sb.AppendLine("              python -m pytest || true");
                sb.AppendLine($"              cp -r . $(Build.ArtifactStagingDirectory)/drop");
                sb.AppendLine("            displayName: 'Install, Test & Package'");
                sb.AppendLine($"            workingDirectory: {srcPath}");
                sb.AppendLine();
                break;

            default:
                sb.AppendLine("          - script: |");
                sb.AppendLine("              echo 'Build steps not configured — add custom build command'");
                sb.AppendLine("            displayName: 'Build (manual configuration required)'");
                sb.AppendLine($"            workingDirectory: {srcPath}");
                sb.AppendLine();
                break;
        }
    }
