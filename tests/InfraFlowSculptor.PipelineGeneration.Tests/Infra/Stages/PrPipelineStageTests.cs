using InfraFlowSculptor.GenerationCore.Models;
using InfraFlowSculptor.PipelineGeneration.Infra;
using InfraFlowSculptor.PipelineGeneration.Infra.Stages;

namespace InfraFlowSculptor.PipelineGeneration.Tests.Infra.Stages;

/// <summary>
/// Unit tests for <see cref="PrPipelineStage"/>.
/// </summary>
public sealed class PrPipelineStageTests
{
    private readonly PrPipelineStage _sut = new();

    [Fact]
    public void Order_Is_200()
    {
        _sut.Order.Should().Be(200);
    }

    [Fact]
    public void Given_Context_When_Execute_Then_AddsPrPipelineFile()
    {
        // Arrange
        var context = CreateContext(isMonoRepo: false);

        // Act
        _sut.Execute(context);

        // Assert
        context.Files.Should().ContainKey("pr.pipeline.yml");
        context.Files["pr.pipeline.yml"].Should().Contain("# PR Validation Pipeline — Auto-generated by InfraFlowSculptor");
    }

    [Fact]
    public void Given_MonoRepoContext_When_Execute_Then_StillAddsPrPipelineFile()
    {
        // Arrange
        var context = CreateContext(isMonoRepo: true);

        // Act
        _sut.Execute(context);

        // Assert
        context.Files.Should().ContainKey("pr.pipeline.yml");
        context.Files["pr.pipeline.yml"].Should().Contain("../Common/pipelines/pr.pipeline.yml");
    }

    [Fact]
    public void Given_StandardContext_When_Execute_Then_UsesStandardTemplatePath()
    {
        // Arrange
        var context = CreateContext(isMonoRepo: false);

        // Act
        _sut.Execute(context);

        // Assert
        context.Files["pr.pipeline.yml"].Should().Contain("../.azuredevops/pipelines/pr.pipeline.yml");
    }

    private static InfraPipelineContext CreateContext(bool isMonoRepo) => new()
    {
        Request = new GenerationRequest(),
        ConfigName = "my-config",
        IsMonoRepo = isMonoRepo,
    };
}
