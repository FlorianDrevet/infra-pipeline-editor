using InfraFlowSculptor.GenerationCore.Models;
using InfraFlowSculptor.PipelineGeneration.Infra;
using InfraFlowSculptor.PipelineGeneration.Infra.Stages;

namespace InfraFlowSculptor.PipelineGeneration.Tests.Infra.Stages;

/// <summary>
/// Unit tests for <see cref="CiPipelineStage"/>.
/// </summary>
public sealed class CiPipelineStageTests
{
    private readonly CiPipelineStage _sut = new();

    [Fact]
    public void Order_Is_100()
    {
        _sut.Order.Should().Be(100);
    }

    [Fact]
    public void Given_Context_When_Execute_Then_AddsCiPipelineFile()
    {
        // Arrange
        var context = CreateContext(isMonoRepo: false);

        // Act
        _sut.Execute(context);

        // Assert
        context.Files.Should().ContainKey("ci.pipeline.yml");
        context.Files["ci.pipeline.yml"].Should().Contain("# CI Pipeline — Auto-generated by InfraFlowSculptor");
    }

    [Fact]
    public void Given_MonoRepoContext_When_Execute_Then_StillAddsCiPipelineFile()
    {
        // Arrange
        var context = CreateContext(isMonoRepo: true);

        // Act
        _sut.Execute(context);

        // Assert
        context.Files.Should().ContainKey("ci.pipeline.yml");
        context.Files["ci.pipeline.yml"].Should().Contain("../Common/pipelines/ci.pipeline.yml");
    }

    [Fact]
    public void Given_StandardContext_When_Execute_Then_UsesStandardTemplatePath()
    {
        // Arrange
        var context = CreateContext(isMonoRepo: false);

        // Act
        _sut.Execute(context);

        // Assert
        context.Files["ci.pipeline.yml"].Should().Contain("../.azuredevops/pipelines/ci.pipeline.yml");
    }

    private static InfraPipelineContext CreateContext(bool isMonoRepo) => new()
    {
        Request = new GenerationRequest(),
        ConfigName = "my-config",
        IsMonoRepo = isMonoRepo,
    };
}
